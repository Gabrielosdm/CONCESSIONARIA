class Conta:
    def __init__(self,nome,numero,tipo):
        self.nome=nome
        self.numero=numero
        self.tipo=tipo

    class Fisica:
        def __init__(self,tipof,cpf):
            tipof.self= tipof
            cpf.self=cpf
    class Juridica:
        def __init__(self,cnpj,empresa):
            cnpj.self=cnpj
            empresa.self=empresa